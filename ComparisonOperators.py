# Comparison operators
# Return a boolean
x = 12

# Greater than, less than: >,<
result = x > 0
print('Is x:', x, 'greater than 0 ' + str(result))

# Greater than or equal, less than or equal: >=, <=
result = x <= 11
print('Is x:', x, 'less than or equal 11 ' + str(result))

# Equal: ==
result = x == 0
print('Is x:', x, 'equal to 0 ' + str(result))

# Not equal: !=
result = x != 0
print('Is x:', x, 'not equal to 0 ' + str(result))
